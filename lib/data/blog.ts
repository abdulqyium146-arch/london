export function getFeaturedPosts(count = 3): BlogPost[] {
  return blogPosts.filter((p) => p.featured).slice(0, count);
}

export function getRecentPosts(count = 5): BlogPost[] {
  return [...blogPosts]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, count);
}

